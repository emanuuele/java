package exercicios;

public class Calculadora {
	public static float somar(float num1, float num2) {
		return num1 + num2;
	}
	public static float somar(float num1, float num2, float num3) {
		return num1 + num2 + num3;
	}
	public static float dividir(float num1, float num2) {
		return num1 / num2;
	}
	public static float dividir(float num1, float num2, float num3) {
		return num1 / num2 / num3;
	}
	public static float multiplicar(float num1, float num2) {
		return num1 * num2;
	}
	public static float multiplicar(float num1, float num2, float num3) {
		return num1 * num2 * num3;
	}
}
